package com.mz.jarboot.core.cmd.view.element;

import org.junit.Test;
import static org.junit.Assert.assertEquals;

/**
 * @author majianzheng
 */
public class ElementTest {
    @Test
    public void testToHtml() {
        Element element = new Element("test");
        assertEquals("test", element.toHtml());
        element.color("red");
        assertEquals("<span style=\"color:red\">test</span>", element.toHtml());
        element.color("#1f359a");
        element.setText("demo").color("green");
        assertEquals("<span style=\"color:green\">demo</span>", element.toHtml());

        element = new Element();
        assertEquals("", element.toHtml());
    }
}
